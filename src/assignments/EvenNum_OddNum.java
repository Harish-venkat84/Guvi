package assignments;

public class EvenNum_OddNum {

	public static void main(String[] args) {
		
		int num = 10;
		
		if (num % 2 == 0) {
			
			System.out.println("Even Number");
			
		}else {
			
			System.out.println("Odd Number");
		}
		
		
		
		int l = 10;
		int w = 5;
		
		l = l * 2;
		w = w * 2;
		
		System.out.println("perimeter of a rectangle ==> "+(l+w));
		
	}
	
}
